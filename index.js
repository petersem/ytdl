async function SearchAndGrab(movieTitle, imdbID) {
  await youtubesearchapi
    .GetListByKeyword(movieTitle + " movie theme", true)
    .then((res) => {
      console.log(
        "Attempting DL:" +
          movieTitle +
          ": " +
          res.items[0].title +
          " (" +
          res.items[0].id +
          ")"
      );
      running++;
      videoDownloader(res.items[0].id, imdbID);
    });
  return;
}

