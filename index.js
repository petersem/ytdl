// make subdirs and copy title.mp4 to subdir/them.mp4
async function Restructure(title, year) {
  var folder = title.replace(":", "").replace("*", "") + " (" + year + ")";
  var mp4File = title.replace(":", "").replace("*", "") + ".mp4";

  // check if file exists
  try {
    if (fs.existsSync("./vid/" + mp4File)) {
      //file exists, not create dir and move
      if (!fs.existsSync("./movies/" + folder)) {
        console.log("Creating dir: ./movies/" + folder);
        fs.mkdirSync("./movies/" + folder, {
          recursive: true,
        });
      }

      fs.rename(
        "./vid/" + mp4File,
        "./movies/" + folder + "/theme.mp4",
        function (err) {
          if (err) {
            throw err;
          } else {
            console.log("Moving file:" + "./vid/" + mp4File + "to: ./movies/" + folder + "/theme.mp4");
          }
        }
      );
    }
  } catch (err) {
    console.error(err);
  }

  return;
}

