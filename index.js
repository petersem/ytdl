async function sleep(milliseconds) {
  const date = Date.now();
  let currentDate = null;
  console.log("Sleeping " + milliseconds / 1000 + " seconds");
  do {
    currentDate = Date.now();
  } while (currentDate - date < milliseconds);
  return;
}

